    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        dem1=1;
        dem2=1;
        position=jTable1.rowAtPoint(evt.getPoint());
        String malhp=jTable1.getValueAt(position, 1).toString();
        DefaultTableModel model=(DefaultTableModel) jTable3.getModel();
        model.setRowCount(0);
        if(ms.length()==6){
            for(LopHocPhan lhp :listhp){
                if(lhp.getMaLHP().equals(malhp)){
                    SinhVien_Helper help=new SinhVien_Helper();
                    model.addRow(help.DisplayTable2(lhp));
                }
            }
        }
        else{
            LopHocPhan l=new LopHocPhan();
            l=listlhp.xemdiem(malhp, jTable1.getValueAt(position, 0).toString());
            SinhVien_Helper help=new SinhVien_Helper();
            model.addRow(help.DisplayTable2(l));
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void rb_mssvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rb_mssvActionPerformed
        dem1=0;dem2=0;
        if(txt_ma.getText().equals(null)||txt_ma.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Text khong duoc de trong", "Canh bao: ", JOptionPane.WARNING_MESSAGE);
            buttonGroup1.clearSelection();
            return;
        }
        DefaultTableModel modelb=(DefaultTableModel) jTable1.getModel();
        modelb.setRowCount(0);//de reset table
        listsv=new JdbcSinhVien();
        list=listsv.select(txt_ma.getText());
        
        if(!list.isEmpty()){
            for(SinhVien sv :list){
               // JOptionPane.showMessageDialog(this,sv.getMSSV());
                SinhVien_Helper help=new SinhVien_Helper();
                modelb.addRow(help.DisplayTable(sv));

            }
            jTable1.setRowSelectionInterval(0,0);
            DisplayCombobox();
        }
        else{
            JOptionPane.showMessageDialog(null, "Nhap sai ma", "Canh bao: ", JOptionPane.WARNING_MESSAGE);
            buttonGroup1.clearSelection();
            return;
        }
        
    }//GEN-LAST:event_rb_mssvActionPerformed

