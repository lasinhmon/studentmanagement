    private void xemDiem(){
        listlhp=new JdbcLopHocPhan();
        if(ms.length()==4)
            listhp=listlhp.select3(ms);
        else
            listhp=listlhp.select2(ms);
        DefaultTableModel model=(DefaultTableModel) jTable3.getModel();
        model.setRowCount(0);
        for(LopHocPhan lhp :listhp){
           // System.out.println(cb_nam.getSelectedItem().toString());
            if(lhp.getHocKy()==Integer.parseInt(cb_hocki.getSelectedItem().toString())&&lhp.getNienKhoa().equals(cb_nam.getSelectedItem().toString())){
                SinhVien_Helper help=new SinhVien_Helper();
                model.addRow(help.DisplayTable2(lhp));
            }
            else if(cb_nam.getSelectedItem().toString().equals("All")){
                SinhVien_Helper help=new SinhVien_Helper();
                model.addRow(help.DisplayTable2(lhp));
            }
        }
        
    }
    
